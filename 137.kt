fun main(){
    // числа
    var q: Int = 12 ;println(q)
    val w: Byte = 11 ;println(w)
    val e:Short=13; println(e)
    var r:Long =10; println(r)
    var t:UInt=14U;println(t)
    val y: UByte =9U; println(y)
    val u:UShort=15U;println(u)
    var i: ULong=8U;println(i)
    // Плавающая точка
    var o: Float=1.6F;println(o)
    val p: Double=0.7;println(p)
    //Символы
    val l: Char = 'q';println(l)
    //Строки
    var k:String ="qwe";println(k)
    //Логика
    var j : Boolean= true; println(j)
}
